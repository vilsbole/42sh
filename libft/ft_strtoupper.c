#include "libft.h"

char	*ft_strtoupper(char *s)
{
	int		i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		if (97 <= s[i] && s[i] <= 122)
			s[i] -= 32;
		i++;
	}
	return (s);
}
