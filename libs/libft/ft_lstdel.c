#include <stdlib.h>
#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*tmp;

	while (*alst != NULL)
	{
		tmp = *alst;
		(*del)(tmp->content, tmp->content_size);
		*alst = tmp->next;
		free((void *)tmp);
	}
	*alst = NULL;
}
