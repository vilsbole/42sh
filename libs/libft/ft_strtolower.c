#include "libft.h"

char	*ft_strtolower(char *s)
{
	int		i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		if (65 <= s[i] && s[i] <= 90)
			s[i] += 32;
		i++;
	}
	return (s);
}
