#include <stdlib.h>
#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*tmp;

	tmp = *alst;
	(*del)(tmp->content, tmp->content_size);
	free((void *)tmp);
	*alst = NULL;
}
