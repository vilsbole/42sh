#include "libft.h"

int		ft_strccmp(const char *s1, const char *s2, int c)
{
	int		i;

	i = 0;
	while (s1[i] != (char)c && s1[i] == s2[i] && s1[i] != '\0')
		i++;
	return (s1[i] - s2[i]);
}
